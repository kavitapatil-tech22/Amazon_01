package ConditionalPrograms;

public class IfElseIfCondStatment_Class {

	public static void main(String[] args) 
	{
		int age=25;
		if(age==25)
		{
			System.out.println("I can vote");
		}
		else if(age==19)
		{
			System.out.println("I can vote 1");
		}
		else if(age==20)
		{
			System.out.println("I can vote 2");
		}
		else if(age==21)
		{
			System.out.println("I can vote 3");
		}
		else
		{
			System.out.println("I can't vote");

		}		

	}
	

}
