package ForLoop_Programs;

public class ForLoop3 {
  // Assignment 24 : Print Numbers from -100 to 0
	public static void main(String[] args) {
		System.out.println("Numbers from -100 to 0");
		//for(int i=0;i>=-100;i--) // printing 0 to -100
		for(int i=-100;i<=0;i++) // printing -100 to 0		
		{			
	       System.out.println(i);
		}
	}

}
