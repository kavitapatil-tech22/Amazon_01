package kavitaBasicPrograms;

public class OperatorPrgm {

	public static void main(String[] args) {
		
		int age =65;
		double salary=10000;
		
		if(age==24 && salary==10000)
		{
			System.out.println("kavita");
		}
		if(age==25 && salary==10000) 
		{
			System.out.println("Santosh");
		}
		if(age>=65 && salary==10000) 
		{
			System.out.println("Vivaan");
		}
		
		if(age>=65 || salary==10000) 
		{
			System.out.println("Aahan");
		}
	}
}
